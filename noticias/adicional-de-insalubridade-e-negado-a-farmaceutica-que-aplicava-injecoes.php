                    <p class="regular-txt">Uma empresa de informática do município da Serra deverá ser indenizada por uma importadora em R$ 19.500 a títulos de danos materiais, por não receber o produto comprado dentro do prazo estabelecido. A decisão é 5ª Vara Cível da Comarca.</p>                     
                    <p class="regular-txt">A 11ª Turma do Tribunal Regional do Trabalho da 4ª Região negou adicional de insalubridade a uma farmacêutica que aplicava injeções. A decisão reformou, no aspecto, sentença do juízo da 28ª Vara do Trabalho de Porto Alegre. Os desembargadores entenderam que não há como enquadrar as atividades da reclamante como insalubres, porque suas tarefas eram prestadas no ambiente da farmácia, o que não pode ser comparado ao trabalho realizado em hospitais, serviços de emergência, enfermarias, postos de vacinação e outros estabelecimentos destinados aos cuidados da saúde humana.</p>
                    <p class="regular-txt">Segundo informações do processo, a autora reivindicou o pagamento de adicional de insalubridade alegando exposição a agentes biológicos e trabalho em contato rotineiro com pacientes em estabelecimentos destinados aos cuidados da saúde humana. O pleito foi deferido parcialmente no primeiro grau. O juízo da 28ª VT considerou o fato de a perícia técnica ter concluído que a reclamante exerceu atividades insalubres em grau médio, pela exposição a agentes biológicos em contato rotineiro com pacientes em estabelecimentos destinados aos cuidados da saúde humana. “Deveria ser pago o adicional de insalubridade em grau médio (20%), ao longo de todo o período contratual, com reflexos em aviso-prévio, férias em 1/3, 13° salário horas extras pagas e depósitos de FGTS com 40%, observado o salário-mínimo nacional como base de cálculo da parcela”, declarou o julgador. A reclamada recorreu ao TRT-RS e os desembargadores da 11ª Turma reformaram a sentença.</p>
                    <p class="regular-txt">O relator do acórdão, desembargador Roger Ballejo Villarinho, afirmou que, ao contrário da conclusão do perito, não há como enquadrar as atividades da reclamante no Anexo 14 da NR 15 da Portaria 3.214/78. “As suas tarefas eram prestadas no ambiente da farmácia, o que não pode ser equiparado ao trabalho realizado em hospitais, serviços de emergência, enfermarias, postos de vacinação e outros estabelecimentos destinados aos cuidados da saúde humana”, citou o magistrado. O desembargador também destacou que, nos termos do inciso I da Súmula 448 do TST, não basta a constatação da insalubridade por meio de laudo pericial para que o empregado tenha direito ao respectivo adicional, sendo necessária a classificação da atividade insalubre na relação oficial elaborada pelo Ministério do Trabalho.</p>
                    <p class="regular-txt">A decisão da Turma foi unânime. Também participaram do julgamento as desembargadoras Flávia Lorena Pacheco e Maria Helena Lisot.</p>
