                <div class="content-noticia ">
                    <p class="regular-text">Ao ter o pedido de recuperação judicial aceito pela Justiça, a empresa recuperanda estará, automaticamente, dispensada de apresentar qualquer certidão negativa para a manutenção de sua atividade, na forma do artigo 52, inciso II, da Lei 11.101/05. Ou seja, o dispositivo, ao não discriminar quais as certidões, dá margem à interpretação teleológica de que a dispensa se estende a toda e qualquer certidão exigida.</p>
                    <p class="regular-text">Com esse entendimento, a 5ª Câmara Cível do Tribunal de Justiça do Rio Grande do Sul negou recurso interposto pela Câmara de Comercialização de Energia Elétrica contra decisão que dispensou uma empresa em recuperação de apresentar certidões negativas para poder ingressar no mercado livre de comercialização de energia elétrica. Com a decisão, a empresa pode, inclusive, contratar com o poder público.</p>
                    <p class="regular-text">No agravo de instrumento, a CCEE — que promove leilões de compra e venda de energia — disse que tem o direito de exigir da Conpasul Construção e Serviços Ltda as referidas certidões, reclamando que não foi intimada a se manifestar no processo. Afirmou que a decisão do juízo de origem cerceou seu direito de defesa e violou o princípio da liberdade de contratação, assim como a de associação.</p>
                    <p class="regular-text">O relator do recurso, desembargador Jorge Luiz Lopes do Canto, explicou que a Lei de Recuperação Judicial tem como princípio a preservação da empresa. Assim, seu objetivo primordial é viabilizar a superação da situação de crise financeira do devedor, a fim de permitir a manutenção da fonte produtora, do emprego dos trabalhadores e dos interesses dos credores</p>
                    <p class="regular-text">"Assim, as deliberações nos processos de recuperação judicial devem estar alinhadas com o princípio da preservação da empresa, visando não prejudicar o soerguimento econômico-financeiro da recuperanda, em benefício da coletividade", complementou no acórdão.</p>
                    <p class="regular-text">Além de citar as disposições do artigo 52, inciso II, da Lei 11.101/05, que dispensa expressamente a apresentação de certidões, o relator observou que a regulação da Agência Nacional de Energia Elétrica (Aneel) é de hierarquia inferior ao texto legal de natureza especial, se subordinando, pois, a este.</p>
                    <p class="regular-text">"Assim, não há que se falar em se exigir a apresentação das certidões negativas com base em Portarias do órgão regulador quando estas se contrapõem à lei de recuperação em exame. Dessa forma, deve ser negado provimento ao Agravo de Instrumento, mantendo-se a decisão recorrida em todos os provimentos e razões de decidir", fulminou.</p>
