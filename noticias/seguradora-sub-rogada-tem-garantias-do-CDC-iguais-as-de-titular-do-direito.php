<p class="regular-text">A 3ª turma do STJ negou provimento a recurso especial e confirmou entendimento no sentido de que a seguradora sub-rogada detém as mesmas prerrogativas do titular originário do direito, por força do artigo 786 do Código Civil.</p>
<p class="regular-text">Os ministros analisaram recurso no qual a TAM alegava a prescrição de ação de regresso, proposta por uma seguradora, em decorrência do pagamento, à sua segurada, das avarias ocorridas em mercadorias durante transporte feito pela empresa aérea.</p>
<p class="regular-text">Em 1º grau, a TAM foi condenada a ressarcir a seguradora em R$ 4.600. A decisão foi mantida pelo TJ/SP, que negou provimento a apelação.</p>
<p class="regular-text">A companhia aérea alegou no STJ a ocorrência de prescrição, entendendo que seria aplicável o prazo de um ano do artigo 206, parágrafo 1°, II, do Código Civil. Pleiteou ainda a aplicação subsidiária do Código Brasileiro de Aeronáutica, e não do CDC, pois, diferentemente do que entendeu o acórdão recorrido, não haveria qualquer relação de consumo entre ela e a recorrida.</p>
<p class="regular-text"><strong>Relação de consumo</strong></p>
<p class="regular-text">A relatora do recurso, ministra Nancy Andrighi, explicou que, como a seguradora se encontra na qualidade de sub-rogada de sua cliente, ela detém todos os direitos e deveres a que esta fazia jus perante a transportadora aérea.</p>
<p class="regular-text">Em seu voto, a ministra citou o julgamento do REsp 1.639.037, em que se afirmou que, “nos contratos de seguro de dano, o segurador, ao pagar a indenização decorrente do sinistro, sub-roga-se nos direitos e nas ações que competirem ao segurado contra o causador do dano, consoante a literal disposição do artigo 786, caput, do CC/2002. Cuida-se, assim, de hipótese de sub-rogação legal, que se opera independentemente da vontade do segurado ou do terceiro responsável pelo dano”.</p>
<p class="regular-text">Dessa forma, a ministra entendeu que, se entre a segurada e a companhia aérea havia uma relação de consumo, a seguradora também terá as prerrogativas inerentes a essa relação.</p>
<p class="regular-text"><strong>Prazo prescricional</strong></p>
<p class="regular-text">A ministra ponderou que a seguradora tem o mesmo prazo para propor a ação contra a TAM que teria o titular originário. “Portanto, possuindo a relação originária a natureza de consumo, a seguradora disporá do prazo prescricional previsto no CDC.”</p>
<p class="regular-text">Ela explicou que, no processo, não ficou bem definida a natureza do relacionamento jurídico existente entre a segurada e a companhia aérea, mas que, ainda assim, não seria aplicável o prazo de um ano de prescrição estabelecido no artigo 206, parágrafo 1°, inciso II, do CC/02, pois o dispositivo trata apenas da relação entre segurador e segurado.</p>
<p class="regular-text">A relatora disse ainda que, mesmo se aplicando ao caso o prazo do artigo 317, I, do CBA – que é de dois anos a partir da data em que se verificou o dano da mercadoria –, a pretensão da seguradora sub-rogada não estaria prescrita, pois o conhecimento da avaria ocorreu em 5 de novembro de 2012 e a ação foi ajuizada em 4 de novembro de 2014.</p>
<p class="regular-text"><strong>Jurisprudência</strong></p>
<p class="regular-text">A relatora lembrou que é pacífica a jurisprudência do STJ no sentido de serem inaplicáveis as indenizações tarifadas previstas no CBA e na Convenção para a Unificação de Certas Regras Relativas ao Transporte Aéreo Internacional (Convenção de Varsóvia), com as modificações dos Protocolos da Haia e de Montreal (decreto 5.910/06), seja para as relações jurídicas de consumo, seja para as estabelecidas entre sociedades empresárias, sobretudo se os danos oriundos da falha do serviço de transporte não resultarem dos riscos inerentes ao transporte aéreo.</p>
<p class="regular-text">“Dessa forma, o TJSP, ao considerar que é integral a reparação pelo dano da mercadoria durante o transporte aéreo, alinhou-se ao entendimento do STJ. Ademais, o tema é abordado de forma praticamente exaustiva no recente julgamento desta Terceira Turma no REsp 1.289.629.”</p>
