<p class="regular-txt">Juíza substituta do 5º Juizado Especial Cível de Brasília julgou improcedente o pedido de indenização por danos morais e materiais feito por quatro passageiros contra a Uber do Brasil Tecnologia Ltda. Os autores alegaram que perderam um voo internacional devido ao atraso no transporte disponibilizado pelo aplicativo, do hotel ao aeroporto.</p>
<p class="regular-txt">Os passageiros pediram indenização por danos materiais, de R$ 386,82, e por danos morais, no valor de R$ 2 mil para cada. Em contestação, a empresa ré alegou culpa exclusiva dos requerentes, sua ilegitimidade passiva, afirmou que não há relação de consumo entre as partes, questionou a legitimidade ativa de dois dos autores – e no mérito, defendeu a inexistência de danos materiais e morais.</p>
<p class="regular-txt">A magistrada julgou o caso com base nas regras da legislação consumerista, Lei 8.078/1990 (Código de Defesa do Consumidor): Configura-se a relação de consumo entre o usuário do serviço e o Uber, nos termos do artigo 14 do CDC e jurisprudência das Turmas Recursais. A juíza deixou de apreciar a legitimidade ativa de dois dos autores, conforme artigo 488 do CPC, diante da primazia do julgamento de mérito, já que a sentença era favorável ao réu.</p>
<p class="regular-txt">Da análise dos documentos trazidos ao processo, a magistrada verificou que o voo estava agendado para às 11h05 e que os passageiros solicitaram o transporte da ré às 09h01, chegando no aeroporto às 10h15: Os autores, contudo, não se atentaram ao horário de embarque, pois é indicado que os passageiros cheguem 3 horas antes do horário da decolagem, quando a viagem é internacional, e 1 hora e 30 minutos, quando nacional, conforme informação disponibilizada no próprio site da companhia aérea utilizada pelos autores.</p>
<p class="regular-txt">A juíza considerou evidente que, mesmo que o motorista fizesse o trajeto no tempo alegado pelos autores (de 50 minutos), eles chegariam em horário diverso do recomendado. Assim, concluiu que não existe nexo de causalidade entre o suposto atraso do motorista disponibilizado pela ré e a perda do voo. A magistrada acrescentou que, diante dos atrasos normais que podem ocorrer no trânsito, caberia aos autores a mínima precaução, encaminhando-se ao aeroporto a tempo suficiente para o embarque.</p>
<p class="regular-txt">Por fim, confirmou que, não existindo o nexo de causalidade ou ato ilícito da ré, não há que se falar em danos materiais ou morais, de sorte que nada há a ser indenizado a tais títulos.</p>
<p class="regular-t">Cabe recurso da sentença.</p>
