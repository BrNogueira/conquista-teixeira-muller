                    <p class="regular-text">A Fazenda Nacional não pode utilizar-se da retenção de mercadoria importada como forma de impor o recebimento de tributos ou exigir caução para sua liberação, por meio da interrupção do despacho aduaneiro para reclassificação fiscal (via SISCOMEX), com objetivo único de assegurar o cumprimento da obrigação. </p>                     
<p class="regular-text">Esse foi o entendimento 7ª Turma do TRF 1ª Região para manter a sentença, da 19ª Vara da Seção Judiciária de Minas Gerais que, em mandado de segurança que tinha com objetivo o desembaraço aduaneiro de mercadoria importada, retido em virtude de reclassificação fiscal, falta de pagamento de tributo ou prestação de garantia, julgou procedente o pedido.</p>
<p class="regular-text">Em seu recurso o ente público sustentou a legalidade e correção do procedimento de reclassificação fiscal.</p>
<p class="regular-text">Ao analisar o caso, a relatora desembargadora federal Ângela Catão, destacou que “a Fazenda Nacional não pode se valer a retenção de mercadoria, para interromper despacho aduaneiro via SISCOMEX, com o objetivo único de assegurar o cumprimento da obrigação perante o Fisco, que sequer lavrou o auto de infração”.</p>
<p class="regular-text">Para concluir seu voto a magistrada assegurou não encontrar amparo legal no pedido do Fisco para a reforma da sentença, estando a decisão de acordo com a Súmula 323 do Supremo Tribunal Federal, que assegura ser inadmissível a apreensão de mercadorias como meio coercitivo para pagamento de tributos.</p>
<p class="regular-text">Nesses termos, a 7ª Turma por unanimidade, negou provimento à apelação e à remessa oficial.</p>
