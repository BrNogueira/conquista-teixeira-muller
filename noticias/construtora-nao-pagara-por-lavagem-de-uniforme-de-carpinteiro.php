                    <p class="regular-txt">A Construtora Edisul Ltda., de Porto Alegre, conseguiu, em recurso para o Tribunal Superior do Trabalho, afastar a condenação ao ressarcimento dos custos com a lavagem do uniforme de um carpinteiro. Para a Terceira Turma do TST, a indenização é indevida porque o empregado utilizava roupas comuns.</p>                     
                    <p class="regular-txt">Água e sabão</p>                     
                    <p class="regular-txt">Na reclamação trabalhista, o carpinteiro argumentou que o uniforme (calças e camiseta) utilizado diariamente ficava sujo de graxa, óleos, cimento e "diversos materiais insalubres" e, por isso, precisava ser lavado separadamente das demais roupas. A empresa, na contestação, sustentou que, na construção civil, não há manipulação de graxa e óleo. Disse ainda que fornecia gratuitamente as vestimentas necessárias ao trabalho e que os cimentos, areias e demais resíduos similares são de fácil lavagem, com água e sabão comum.</p>                     
                    <p class="regular-txt">Acréscimo</p>                     
                    <p class="regular-txt">A 8ª Vara do Trabalho de Porto Alegre julgou improcedente o pedido de indenização pela lavagem do uniforme, mas o Tribunal Regional do Trabalho da 4ª Região reformou a sentença para condenar a empresa ao pagamento de R$ 30 mensais ao empregado. De acordo com a jurisprudência do TRT, a necessidade de produtos ou de procedimentos diferenciados em relação à lavagem das roupas de uso comum resulta em acréscimo significativo de produtos de limpeza, água e energia elétrica.</p>                     
                    <p class="regular-txt">Roupas comuns</p>
                    <p class="regular-txt">Para o relator do recurso de revista da empresa, ministro Maurício Godinho Delgado, se o empregado é obrigado a utilizar o uniforme fornecido pela empresa, ou seja, roupa especial, vinculada ao tipo de atividade empresarial, as despesas com a sua higienização devem ser suportadas pelo empregador. Por outro lado, se os uniformes forem roupas comuns, similares às usadas no cotidiano, sem peculiaridades e sem gastos adicionais para a sua higienização, não há como onerar o empregador.</p>
                    <p class="regular-txt">Por unanimidade, a Turma deu provimento ao recurso para determinar a exclusão da condenação da Edisul ao ressarcimento das despesas pela lavagem do uniforme.</p>
