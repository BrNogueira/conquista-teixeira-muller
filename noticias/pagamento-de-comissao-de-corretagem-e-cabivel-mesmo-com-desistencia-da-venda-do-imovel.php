                    <p class="regular-text">A 5ª Turma Cível do Tribunal de Justiça do Distrito Federal condenou um vendedor a pagar comissão de corretagem a uma imobiliária, embora a venda de seu imóvel não tenha sido concretizada. Conforme os autos, a parte autora alegou que o serviço de intermediação foi integralmente cumprido e que a venda do imóvel somente não foi efetivada em razão do arrependimento do réu, manifestado às vésperas da lavratura da escritura.</p>                     
                    <p class="regular-text">Por sua vez, o réu confirmou que o negócio não se concretizou em razão da sua desistência, diante do desgaste emocional sofrido. Defendeu que a comissão de corretagem somente seria devida se consumada a lavratura de escritura pública ou de contrato particular, o que não ocorreu.</p>                     
