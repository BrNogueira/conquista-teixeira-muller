                    <p class="regular-text">O desembargador ressaltou ainda que o réu, de fato, podia exercer o direito de desistir da venda do imóvel de sua propriedade até o registro de venda na escritura. “Contudo, não pode pretender se eximir da consequência relativa à sua decisão que, no caso em tela, refere-se ao pagamento dos serviços de mediação que lhe foram integralmente prestados”, asseverou.</p>                     
                    <p class="regular-text">Assim, a 5ª Turma, de forma unânime, condenou o réu a pagar à imobiliária o valor correspondente a 5% do valor de venda do imóvel previamente estabelecido.</p>                     
