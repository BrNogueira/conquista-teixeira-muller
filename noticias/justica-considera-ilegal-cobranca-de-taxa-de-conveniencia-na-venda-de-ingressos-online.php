                <div class="content-noticia ">
                    <p class="regular-text">A Terceira Turma do Superior Tribunal de Justiça (STJ) restabeleceu sentença que reconheceu a ilegalidade da taxa de conveniência cobrada pelo site Ingresso Rápido na venda on-line de ingressos para shows e outros eventos.</p>
                    <p class="regular-text">O colegiado considerou que a taxa não poderia ser cobrada dos consumidores pela mera disponibilização de ingressos em meio virtual, constatando que a prática configura venda casada e transferência indevida do risco da atividade comercial do fornecedor ao consumidor, pois o custo operacional da venda pela internet é ônus do fornecedor. A ministra Nancy Andrighi, relatora do recurso julgado, explicou que a sentença restabelecida foi proferida no âmbito de uma ação coletiva de consumo, e por isso tem validade em todo o território nacional.</p>                  
                    <p class="regular-text">Na origem do caso julgado pela Terceira Turma, a Associação de Defesa dos Consumidores do Rio Grande do Sul (Adeconrs) moveu a ação coletiva em 2013 contra a Ingresso Rápido e obteve sentença favorável na 16ª Vara Cível de Porto Alegre.</p>
                    <p class="regular-text"><strong>Venda casada</strong></p>
                    <p class="regular-text">O Tribunal de Justiça do Rio Grande do Sul (TJRS) reformou a sentença por entender que a aquisição dos ingressos on-line é uma opção ao consumidor, haja vista se tratar de alternativa à compra presencial, que também é oferecida pela Ingresso Rápido, não sendo sua utilização obrigatória. Para o TJRS, o oferecimento dos ingressos na internet é uma comodidade adicional que gera custos que justificariam a cobrança da taxa, sob pena de enriquecimento ilícito do consumidor.</p>
                    <p class="regular-text">Segundo Nancy Andrighi, uma das formas de violação da boa-fé objetiva é a venda casada, a imposição de uma contratação indesejada de um intermediário escolhido pelo fornecedor, limitando a liberdade de escolha do consumidor.</p>
                    <p class="regular-text">A relatora citou julgado repetitivo da Segunda Seção que adotou o entendimento de que, nos casos de intermediação por meio de corretagem, como não há relação contratual direta entre o corretor e o terceiro (consumidor), quem deve arcar, em regra, com a remuneração do corretor é a pessoa com quem ele se vinculou, ou seja, o fornecedor.</p>
                    <p class="regular-text"><strong>Transferência indevida do risco</strong></p>
                    <p class="regular-text">De acordo com a relatora, “a venda do ingresso para um determinado espetáculo cultural é parte típica e essencial do negócio, risco da própria atividade empresarial que visa o lucro e integrante do investimento do fornecedor, compondo, portanto, o custo embutido no preço”.</p>
                    <p class="regular-text">Ela acrescentou que a venda dos ingressos pela internet alcança interessados em número infinitamente superior ao da venda por meio presencial, privilegiando os interesses dos promotores do evento.</p>
                    <p class="regular-text">Nancy Andrighi destacou que a cobrança da taxa de conveniência pela mera disponibilização dos ingressos na internet transfere aos consumidores parcela considerável do risco do empreendimento, pois os serviços a ela relacionados, remunerados pela taxa de conveniência, deixam de ser suportados pelos próprios fornecedores. Para a ministra, o benefício fica somente para o fornecedor.</p>
                    <p class="regular-text">A vantagem que o consumidor teria ao poder comprar o ingresso sem precisar sair de casa, segundo a ministra, acaba sendo “totalmente aplacada” quando ele se vê obrigado a se submeter, “sem liberdade”, às condições impostas pelo site de venda de ingressos e pelos promotores do evento, o que evidencia que a disponibilização de ingressos via internet foi instituída exclusivamente em favor dos fornecedores.</p>
                    <p class="regular-text">A ministra lembrou que no, caso analisado, não há declaração clara e destacada de que o consumidor está assumindo um débito que é de responsabilidade do incumbente – o promotor ou produtor do espetáculo cultural – “não se podendo, nesses termos, reconhecer a validade da transferência do encargo”.</p>
                    <p class="regular-text"><strong>Dano moral coletivo</strong></p>
                    <p class="regular-text">Nancy Andrighi afirmou que o dano moral coletivo pedido na ação não ficou caracterizado, já que a ilegalidade verificada não atinge valores essenciais da sociedade, configurando mera infringência à lei ou ao contrato em razão da transferência indevida de um encargo do fornecedor ao consumidor.</p>
