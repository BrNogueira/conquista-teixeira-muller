                <div class="content-noticia ">
                    <p class="regular-text">A Corte Especial do Superior Tribunal de Justiça (STJ) definiu que é de dez anos o prazo de prescrição para o ajuizamento de ação de repetição de indébito por cobrança indevida de serviços de telefonia não contratados.</p>
                    <p class="regular-text">O entendimento da Corte é que tais cobranças devem seguir a norma geral prevista no artigo 205 do Código Civil, de dez anos, a exemplo do que foi decidido pelo STJ nos casos de ação de repetição de indébito de tarifas de água e esgoto (a jurisprudência foi consolidada na Súmula 412).</p>
                    <p class="regular-text">Nos embargos de divergência analisados pela Corte Especial, o recorrente alegou divergência entre acórdãos da Primeira e da Segunda Seção do tribunal. Foram citados acórdãos das turmas de direito privado que estabeleceram o prazo prescricional de três anos, com base na aplicação do artigo 206, parágrafo 3º, do CC, ao passo que os julgamentos das turmas de direito público fixavam o prazo de dez anos com apoio na regra geral.</p>
                    <p class="regular-text">Para o relator do recurso na Corte Especial, ministro Og Fernandes, a questão de repetição de indébito por serviços cobrados que não foram contratados não diz respeito a enriquecimento sem causa, o que poderia justificar a aplicação do prazo trienal. Ele explicou que, no caso analisado, há relação contratual entre a operadora e o consumidor e, portanto, isso diz respeito a um fato do serviço.</p>
                    <p class="regular-text">A discussão sobre a cobrança indevida de valores constantes de relação contratual e eventual repetição de indébito não se enquadra na hipótese do artigo 206, parágrafo 3º, IV, do Código Civil/2002, seja porque a causa jurídica, em princípio, existe (relação contratual prévia em que se debate a legitimidade da cobrança), seja porque a ação de repetição de indébito é ação específica, justificou o relator.</p>
                    <p class="regular-text"><strong>Requisitos</strong></p>
                    <p class="regular-text">O ministro explicou que a pretensão das ações de enriquecimento sem causa possui como requisito o enriquecimento de alguém, o empobrecimento correspondente de outrem, relação de causalidade entre ambos, ausência de causa jurídica e inexistência de ação específica. Trata-se, portanto, de ação subsidiária que depende da inexistência de causa jurídica, resumiu Og Fernandes.</p>
                    <p class="regular-text">Ele citou o Enunciado 188 aprovado na III Jornada de Direito Civil do Conselho da Justiça Federal, segundo o qual a existência de negócio jurídico válido e eficaz é, em regra, uma justa causa para o enriquecimento.</p>
                    <p class="regular-text">Na visão do relator, acompanhada pela maioria dos ministros, a interpretação do prazo estabelecido no artigo 206, parágrafo 3º, deve ser restritiva para os casos subsidiários de ação destinada a recuperar o que foi obtido à custa do prejudicado.</p>
