                    <p class="regular-text">Segundo o relator, a empresa encontra-se regularmente inscrita junto ao Conselho Regional de Química (CRQ). Assim, o art. 1º da Lei 6.839/80 veda a duplicidade de registros nos conselhos profissionais, porquanto o registro das empresas subordina-se à atividade básica ou aos serviços prestados a terceiros, ressaltou o juiz federal. </p>
