                                             <p class="regular-txt">A Subseção II Especializada em Dissídios Individuais (SDI-2) do Tribunal Superior do Trabalho deu provimento ao recurso de um ex-sócio da Sisal Construtora Ltda., de Salvador (BA), que teve parte de sua aposentadoria bloqueada para saldar dívidas trabalhistas. Segundo o colegiado, mesmo em se tratando de execução trabalhista, a penhora foi ilegal e arbitrária.</p>                     
                    <p class="regular-txt"><strong>Bloqueio</strong></p>
                    <p class="regular-txt">Em dezembro de 2015, o juízo da 9ª Vara do Trabalho de Salvador determinou o bloqueio de 20% dos proventos da aposentadoria do sócio para o pagamento de parcelas devidas a um pedreiro da Sisal. Ele então impetrou mandado de segurança no Tribunal Regional do Trabalho da 5ª Região (BA), requerendo a concessão de liminar para determinar a sustação da ordem de bloqueio e a devolução dos valores acaso já bloqueados.</p>
                    <p class="regular-txt">Amparado em julgamento de um incidente de uniformização de jurisprudência do seu Tribunal Pleno, o TRT entendeu pela legalidade de penhora parcial dos proventos. Na decisão, o Tribunal Regional chega a reconhecer que a questão é polêmica e tem gerado decisões divergentes nos diversos tribunais, mas conclui que é possível a penhora de até 20% de salário ou proventos para pagamento de créditos trabalhistas, que têm natureza alimentar.</p>
                    <p class="regular-txt"><strong>Impenhorabilidade</strong></p>
                    <p class="regular-txt">O relator do recurso do ex-sócio da empresa, ministro Emmanoel Pereira, lembrou que o TST tem entendido que, em situações assim, deve-se conceder a segurança porque os proventos de aposentadoria são “indispensáveis à subsistência de quem os recebe e de sua família”. Segundo ele, a matéria não comporta mais discussão no âmbito da SDI-2 e está pacificada com a edição da Orientação Jurisprudencial 153, revisada e atualizada em decorrência do Código de Processo Civil de 2015.</p>
                    <p class="regular-txt">Com a decisão, a subseção deverá enviar ofício à 9ª Vara de Trabalho de Salvador para cassar a ordem de bloqueio dos proventos do ex-sócio da Sisal.</p>
