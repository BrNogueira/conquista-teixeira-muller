                    <p class="regular-text">A Quinta Turma do Tribunal Superior do Trabalho afastou a responsabilidade solidária da Contécnica Consultoria Técnica Ltda., de São Paulo (SP), pelo pagamento de parcelas devidas a uma contadora da Serpal Engenharia e Construtora Ltda. Segundo o entendimento do TST, a existência de sócios em comum não é suficiente para a configuração de grupo econômico.</p>
                    <p class="regular-text"><strong>Falência</strong></p>
                    <p class="regular-text">Na reclamação trabalhista, a contadora disse que havia sido admitida pela Serpal em 2009 por meio de contrato de prestação de serviços como gerente contábil e fiscal de todas as empresas do grupo denominado Advento, que reunia outras cinco empresas. A construtora, no entanto, teve sua falência decretada em 2014. Por isso, a gerente pediu a condenação das demais empresas, entre elas a Contécnica, a responder solidariamente por parcelas decorrentes do reconhecimento do vínculo de emprego, entre outras.</p>
                    <p class="regular-text"><strong>Grupo econômico</strong></p>
                    <p class="regular-text">O Tribunal Regional do Trabalho da 2ª Região manteve a condenação à responsabilidade solidária, ao fundamento de que havia elementos suficientes para a configuração de grupo econômico. Segundo o TRT, os documentos constantes dos autos permitiam concluir que havia coordenação entre a Contécnica e as demais empresas e que ela havia se beneficiado da prestação dos serviços da contadora, ainda que indiretamente, por meio do grupo.</p>
                    <p class="regular-text"><strong>Vínculo hierárquico</strong></p>
                    <p class="regular-text">O relator do recurso de revista da Contécnica, ministro Douglas Alencar Rodrigues, explicou que o TST, ao interpretar o artigo 2º, parágrafo 2º, da CLT, pacificou o entendimento de que a mera existência de sócios em comum e de relação de coordenação entre as empresas não constitui fator suficiente para a configuração de grupo econômico. “Revela-se imprescindível a existência de vínculo hierárquico entre elas, isto é, de efetivo controle de uma empresa líder sobre as demais, o que não foi constatado”, concluiu.</p>
                    <p class="regular-text">A decisão foi unânime.</p>
